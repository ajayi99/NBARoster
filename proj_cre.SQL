
CREATE TABLE TEAM (
TEAM_NO VARCHAR2(2) PRIMARY KEY,
TEAM_NAME VARCHAR2(15) NOT NULL,
TEAM_LOCATION VARCHAR2(20) NOT NULL,
ARENA VARCHAR2(25) NOT NULL UNIQUE
);

CREATE TABLE PLAYER (
PLAYER_NO VARCHAR2(2) PRIMARY KEY,
P_LASTNAME VARCHAR2(35) NOT NULL,
P_FIRSTNAME VARCHAR2(35) NOT NULL,
POSITION VARCHAR2(2) NOT NULL,
P_SALARY VARCHAR(10) NOT NULL,
COLLEGE VARCHAR2(35),
HEIGHT VARCHAR2(4),
BIRTHDAY DATE,
TEAM_NO VARCHAR2(2) REFERENCES TEAM (TEAM_NO)
);

CREATE TABLE COACH (
COACH_NO VARCHAR2(3) PRIMARY KEY,
C_FIRSTNAME VARCHAR2(35) NOT NULL,
C_LASTNAME VARCHAR2(35) NOT NULL,
SALARY VARCHAR2(8) NOT NULL,
POSITION VARCHAR2(15) NOT NULL,
BIRTHDAY DATE,
TEAM_NO VARCHAR2(2) REFERENCES TEAM (TEAM_NO)
);

CREATE TABLE GAME (
GAME_NO VARCHAR2(3) PRIMARY KEY,
GAME_DATE DATE,
GAME_OUTCOME VARCHAR2(1) NOT NULL,
ARENA VARCHAR2(25) REFERENCES TEAM(ARENA),
OPPONENT_NO VARCHAR2(2) REFERENCES TEAM(TEAM_NO)
);


CREATE TABLE PERFORMANCE (
PERFORMANCE_NO VARCHAR2(3) PRIMARY KEY,
POINTS VARCHAR2(2),
REBOUNDS VARCHAR2(2),
ASSISTS VARCHAR2(2),
BLOCKS VARCHAR2(2),
STEALS VARCHAR2(2),
PLAYER_NO VARCHAR2(2) REFERENCES PLAYER(PLAYER_NO),
GAME_NO VARCHAR(3) REFERENCES GAME(GAME_NO)
);

